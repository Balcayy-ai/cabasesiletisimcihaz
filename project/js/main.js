    nextBtn?.addEventListener("click", () => show(index + 1));
    dots.forEach((dot, j) => {
      dot.addEventListener("click", () => show(j));
    });

    root.addEventListener("mouseenter", clearAutoplay);
    root.addEventListener("mouseleave", startAutoplay);
